package com.boder.diary_dto;

public class PhotoDTO {

    private int photo_pk;
    private String photo_id;
    private String photo_num;
    private String photo_title;
    private String photo_content;
    private String photo_date;
    private String photo_file;

    public int getPhoto_pk() {
        return photo_pk;
    }

    public void setPhoto_pk(int photo_pk) {
        this.photo_pk = photo_pk;
    }

    public String getPhoto_id() {
        return photo_id;
    }

    public void setPhoto_id(String photo_id) {
        this.photo_id = photo_id;
    }

    public String getPhoto_num() {
        return photo_num;
    }

    public void setPhoto_num(String photo_num) {
        this.photo_num = photo_num;
    }

    public String getPhoto_title() {
        return photo_title;
    }

    public void setPhoto_title(String photo_title) {
        this.photo_title = photo_title;
    }

    public String getPhoto_content() {
        return photo_content;
    }

    public void setPhoto_content(String photo_content) {
        this.photo_content = photo_content;
    }

    public String getPhoto_date() {
        return photo_date;
    }

    public void setPhoto_date(String photo_date) {
        this.photo_date = photo_date;
    }

    public String getPhoto_file() {
        return photo_file;
    }

    public void setPhoto_file(String photo_file) {
        this.photo_file = photo_file;
    }


}
